ALTER TABLE credit_products_db.USER_CREDIT_SCORE
ADD COLUMN score INTEGER;

UPDATE credit_products_db.USER_CREDIT_SCORE
SET score = CASE
    WHEN CREDIT_ID = 'CREDIT001' THEN 750
    WHEN CREDIT_ID = 'CREDIT002' THEN 680
    WHEN CREDIT_ID = 'CREDIT003' THEN 720
    WHEN CREDIT_ID = 'CREDIT004' THEN 800
    WHEN CREDIT_ID = 'CREDIT005' THEN 590
END
WHERE CREDIT_ID IN ('CREDIT001', 'CREDIT002', 'CREDIT003', 'CREDIT004', 'CREDIT005');

